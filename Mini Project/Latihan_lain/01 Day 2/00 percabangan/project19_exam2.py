angka_rahasia = 7
tebakan = int(input("Tebak angka rahasia (1-10): "))

while tebakan != angka_rahasia:
    print("Tebakanmu salah. Coba lagi.")
    tebakan = int(input("Tebak angka rahasia (1-10): "))
print("Selamat! Tebakanmu benar.")


