for i in range(5):
    print("Halo ke-", i)
print("Selesai")

# Output:
# Halo ke- 0
# Halo ke- 1
# Halo ke- 2
# Halo ke- 3
# Halo ke- 4
# Selesai
# Penjelasan:
# - Fungsi range(5) menghasilkan urutan angka dari 0 hingga 4.
# - Looping for digunakan untuk mengulangi pernyataan di dalamnya sebanyak 5 kali.
# - Pada setiap iterasi, nilai i akan bertambah 1 dimulai dari 0 hingga 4.
# - Pada setiap iterasi, program menc
#etak "Halo ke-" diikuti dengan nilai i saat ini.
# - Setelah loop selesai, program mencetak "Selesai".
# - Dengan demikian, output yang dihasilkan adalah "Halo ke-" diikuti dengan angka dari 0 hingga 4, diakhiri dengan "Selesai".