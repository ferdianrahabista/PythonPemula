gaji = float(input("Masukkan gaji pokok :"))
if gaji > 10000000:
    bonus = gaji * 0.20
elif gaji > 5000000:
    bonus = gaji * 0.10
else:
    bonus = gaji * 0.2


print("Bonus yang didapatkan adalah : ", bonus)
print("Total gaji + bonus adalah : ", gaji + bonus)
print("Gaji pokok adalah : ", gaji)
print("Gaji pokok + bonus adalah : ", gaji + bonus)

