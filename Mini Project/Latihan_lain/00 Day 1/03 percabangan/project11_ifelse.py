nilai = 50
if nilai >= 60:
    print("Lulus")
else:
    print("Tidak Lulus")
# Output: Tidak Lulus
# Penjelasan:
# Pada kode di atas, kita menggunakan pernyataan if untuk memeriksa apakah nilai lebih besar atau sama dengan 60.
# Jika kondisi tersebut benar, maka program akan mencetak "Lulus".
# Jika tidak, program akan mencetak "Tidak Lulus".
# Pada contoh ini, nilai adalah 50, sehingga program mencetak "Tidak Lulus".
